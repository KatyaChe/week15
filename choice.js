function color() {
    if (document.getElementById("colors").value == "yell") 
        document.getElementById("result").style.backgroundColor="#FFFF00";
        if (document.getElementById("colors").value == "green") 
        document.getElementById("result").style.backgroundColor="green";
        if (document.getElementById("colors").value == "orange") 
        document.getElementById("result").style.backgroundColor="orange";
 }